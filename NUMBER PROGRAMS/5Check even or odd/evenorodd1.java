class evenorodd1{
	public static void main(String[] args){
		int num=12;
		if(num>0){
			while(num>0){
				num=num-2;
				}
				if(num==0){
					System.out.println("Even");
					}
					else{
						System.out.println("Odd");
						}
		}
		else{
			System.out.println("Invalid Number");
			}
	}
}		
